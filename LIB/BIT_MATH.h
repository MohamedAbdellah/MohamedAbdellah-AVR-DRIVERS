#ifndef _BIT_MATH_H_
#define _BIT_MATH_H_

#include"STD_types.h"

#define SETBIT(REG,BIT)     REG |= (1<<BIT); 
#define TOGGLEBIT(REG,BIT)  REG ^= (1<<BIT); 
#define CLEARBIT(REG,BIT)   REG &= (~(1<<BIT)); 
#define GITBIT(REG,BIT)     ((REG>>BIT)&1); 
#define ASSIGNBIT(REG,BIT,VALUE)   (REG = (~(1<<BIT)& REG ) | (VALUE<<BIT)); 



#endif
